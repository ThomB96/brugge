function kleurVeranderen1() {
    var check = document.getElementById('bingoButton1').checked;
    if (check == true) {
        document.getElementById('bingoTekst1').style.color = '#04AA6D'
    }
    else if(check == false) {
        document.getElementById('bingoTekst1').style.color = 'black'
    }
};
